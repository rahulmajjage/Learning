#Counter
#Demonstrates the for loop with range function.

print ("Counting:")
for i in range(10):
    print (i),
print ("Counting by fives:")
for i in range (0, 50, 5):
    print (i) ,
print ("Counting backwards:")
for i in range (10, 0, -1):
    print (i,)

