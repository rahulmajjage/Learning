#Using String methods

quote="\tWhere there is a Will, there is a way!\t\t "
quote1="where there is a Will, there is a way!\t\t "
print ("Original Quote: ", quote)
print ("UPPERCASE: \t",quote.upper())
print ("lowercase: \t", quote.lower())
print ("Remove space and tabs: ",quote.strip())
print ("Capitalize:\t\t",quote1.capitalize())
print ("New Title: \t",quote.title())
print ("Swapping cases: ",quote.swapcase())
print ("Replacing Strings: ",quote.replace("a way","always a way"))
print ("\nOriginal Quote: ", quote)
